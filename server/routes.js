var controllers = require('./controllers/controllers.js');
var router = require('express').Router();

for (var route in controllers) {
	if(route === "questions") {
	  router.route("/" + route)
	    .get(controllers[route].get);
	}
	else if(route === "users") {
    router.route("/" + route)
	    .get(controllers[route].get)
	    .post(controllers[route].post);
	}
}

module.exports = router;